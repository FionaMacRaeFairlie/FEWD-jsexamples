// Comparison operators are used in logical statements to 
// determine equality or difference between variables or values
let x = 5;

// == operator compares value
console.log("x==8: " + (x==8));
console.log("x==5: " + (x==5));
console.log('x=="5": ' + (x=="5"));

// === operator compares value and type
console.log("x===5: " + (x===5));
console.log('x==="5": ' + (x==="5"));
